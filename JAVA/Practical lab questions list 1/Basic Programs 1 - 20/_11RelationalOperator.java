class _11RelationalOperator
{
	public static void main(String[] args)
	{
		int a = 10, b = 6;
		System.out.println(a + " < " + b + " : " + (a < b));
		System.out.println(a + " <= " + b + " : " + (a <= b));
		System.out.println(a + " > " + b + " : " + (a > b));
		System.out.println(a + " >= " + b + " : " + (a >= b));
		System.out.println(a + " == " + b + " : " + (a == b));
		System.out.println(a + " != " + b + " : " + (a != b));
	}
}