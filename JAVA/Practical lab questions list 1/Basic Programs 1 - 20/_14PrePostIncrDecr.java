class _14PrePostIncrDecr
{
	public static void main(String[] args)
	{
		int a = 10;
		System.out.println("a : " + a + "\ta++ : " + a++);

		a = 10;
		System.out.println("a : " + a + "\t++a : " + ++a);

		a = 10;
		System.out.println("a : " + a + "\ta-- : " + a--);

		a = 10;
		System.out.println("a : " + a + "\t--a : " + --a);
	}
}