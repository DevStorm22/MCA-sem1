class _05AreaCircumferenceCircle
{
	public static void main(String[] args)
	{
		float r = 18.05f;
		float circumference = 2 * 3.14f * r, area = 3.14f * r * r;

		System.out.println("Circumference of the circle of radius " + r + " cm is " + circumference + " cm.\n");
		System.out.println("Area of the circle of radius " + r + " cm is " + area + " sqcm.");
	}
}